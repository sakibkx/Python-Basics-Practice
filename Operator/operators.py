# Arithmetic operators
a = 10
b = 5
print("Arithmetic Operations:")
print(f"a + b = {a + b}")
print(f"a - b = {a - b}")
print(f"a * b = {a * b}")
print(f"a / b = {a / b}")
print(f"a // b = {a // b}")
print(f"a % b = {a % b}")
print(f"a ** b = {a ** b}")

# Comparison operators
print("\nComparison Operations:")
print(f"a == b: {a == b}")
print(f"a != b: {a != b}")
print(f"a > b: {a > b}")
print(f"a < b: {a < b}")
print(f"a >= b: {a >= b}")
print(f"a <= b: {a <= b}")

# Logical operators
x = True
y = False
print("\nLogical Operations:")
print(f"x and y: {x and y}")
print(f"x or y: {x or y}")
print(f"not x: {not x}")

# Assignment operators
c = 20
print("\nAssignment Operations:")
c += 5
print(f"c += 5: {c}")
c -= 5
print(f"c -= 5: {c}")
c *= 2
print(f"c *= 2: {c}")
c /= 2
print(f"c /= 2: {c}")

# Bitwise operators
m = 4  # 0100 in binary
n = 6  # 0110 in binary
print("\nBitwise Operations:")
print(f"m & n: {m & n}")  # AND
print(f"m | n: {m | n}")  # OR
print(f"m ^ n: {m ^ n}")  # XOR
print(f"~m: {~m}")  # NOT
print(f"m << 1: {m << 1}")  # Left shift
print(f"m >> 1: {m >> 1}")  # Right shift

# Membership operators
lst = [1, 2, 3, 4, 5]
print("\nMembership Operations:")
print(f"3 in lst: {3 in lst}")
print(f"6 not in lst: {6 not in lst}")

# Identity operators
p = [1, 2, 3]
q = [1, 2, 3]
print("\nIdentity Operations:")
print(f"p is q: {p is q}")
print(f"p is not q: {p is not q}")

